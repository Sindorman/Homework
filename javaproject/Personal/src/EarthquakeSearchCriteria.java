public class EarthquakeSearchCriteria {
	private static String DateFrom;
	private static String DateTo;
	private static String State;
	private static double Magnitude;
	
	public static String getDateFrom() {
		return DateFrom;
	}
	public void setDateFrom(String dateFrom) {
		this.DateFrom = dateFrom;
	}
	public static String getDateTo() {
		return DateTo;
	}
	public void setDateTo(String dateTo) {
		this.DateTo = dateTo;
	}
	public static String getState() {
		return State;
	}
	public void setState(String state) {
		this.State = state;
	}
	public static double getMagnitude() {
		return Magnitude;
	}
	public void setMagnitude(double magnitude) {
		this.Magnitude = magnitude;
	}
	
	


	
}
